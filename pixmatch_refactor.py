def ScoreEmoji():
    """
    - Si la puntuaci√≥n del jugador es igual a 0, devuelve el emoji 'üòê'.
    - Si la puntuaci√≥n del jugador est√° entre -5 y -1 (inclusive), devuelve el emoji 'üòè'.
    - Si la puntuaci√≥n del jugador est√° entre -10 y -6 (inclusive), devuelve el emoji '‚òπÔ∏è'.
    - Si la puntuaci√≥n del jugador es igual o inferior a -11, devuelve el emoji 'üòñ'.
    - Si la puntuaci√≥n del jugador est√° entre 1 y 5 (inclusive), devuelve el emoji 'üôÇ'.
    - Si la puntuaci√≥n del jugador est√° entre 6 y 10 (inclusive), devuelve el emoji 'üòä'.
    - Si la puntuaci√≥n del jugador es mayor que 10, devuelve el emoji 'üòÅ'.
    """
    if mystate.myscore == 0: return 'üòê'
    elif -5 <= mystate.myscore <= -1: return 'üòè'
    elif -10 <= mystate.myscore <= -6: return '‚òπÔ∏è'
    elif mystate.myscore <= -11: return 'üòñ'
    elif 1 <= mystate.myscore <= 5: return 'üôÇ'
    elif 6 <= mystate.myscore <= 10: return 'üòä'
    elif mystate.myscore > 10: return 'üòÅ'

